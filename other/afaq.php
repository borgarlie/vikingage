	<u>Browser Support</u><br>
	Supported browsers: Chrome, Firefox, Safari, Opera.<br>
	This game does not support Internet Explorer.<br>
	<br>
	<a name='bugs' /></a>
	<u>Bugs</u><br>
	Reporting bugs will give a reward according to the bugs impact on the game.<br>
	The reward depends on how major the bug was, and if anyone else has reported it.<br>
	Bug reports can be sent to: borgardev@gmail.com<br>
	<br>
	<a name='levels' /></a>
	<u>Levels</u><br>
	At each 10th level you will gain a new Title.<br>
	At the same time you will also receive a new quest that you can do.<br>
	You are shielded from PvP for 2 weeks from the creating of your account, or<br>
	until you hit level 5, where your shield will be removed.<br>
	<br>
	<a name='hunting' /></a>
	<u>Hunting</u><br>
	Oak tree is the easiest place to hunt<br>
	Thereafter comes "Rocky Hills Pass", "Mountain Side Caves", "Camp" and at last, "DANGER".<br>
	<br>
	<a name='praying' /></a>
	<u>Praying</u><br>
	Praying for food or wood gives you a chance between 0-100.<br>
	Minerals have a chance between 0-50.<br>
	<br>
	<a name='city' /></a>
	<u>City</u><br>
	At each city level you can maximum buy 20 houses (pop-cap 100).<br>
	This means that at level 1 you can have 100 people in your city.<br>
	At level 2 you can have 200. And so on.<br>
	To upgrade you need to pay 1000 of every resource.<br>
	Bunker space is increased for each City level aswell.<br>
	You have 5 spots at level 1, 10 at level 2, and so on.<br>
